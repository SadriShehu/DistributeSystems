package application;

public class Rruge {
	String ID, Qyteti, Fshati, Rruga;
	
	public Rruge(String ID, String Qyteti, String Fshati, String Rruga) {
		this.ID = ID;
		this.Qyteti = Qyteti;
		this.Fshati = Fshati;
		this.Rruga = Rruga;
	}

	public String getID() {
		return ID;
	}

	public void setID(String iD) {
		ID = iD;
	}

	public String getQyteti() {
		return Qyteti;
	}

	public void setQyteti(String qyteti) {
		Qyteti = qyteti;
	}

	public String getFshati() {
		return Fshati;
	}

	public void setFshati(String fshati) {
		Fshati = fshati;
	}

	public String getRruga() {
		return Rruga;
	}

	public void setRruga(String rruga) {
		Rruga = rruga;
	}
}
